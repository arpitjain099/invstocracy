<center><u><b>Glossary</b></u></center><br>
<b>A shares</b> – A class of shares which have specific rights attached to them, as set out in a company’s articles of association.  Typically A shares have enhanced voting rights or other benefits compared to other classes of shares, such as B shares.
<br><br>
<b>Abatement</b> – A reduction in amount or intensity.  Usually applies to decreases in taxes or rent.
<br><br>
<b>Accrue</b> – To accumulate or increase; generally refers to payments owed but which have not yet been paid. 
<br><br>
<b>Amortization</b> – The gradual paying off of a debt by periodic installments.  Example: a $100,000 loan is arranged at an 8% interest rate.  The borrower pays $10,000 in the first year.  Of that payment, $8,000 is for the interest owed, and the remaining $2,000 serves to amortize the loan balance.  After that payment, the loan balance will have been amortized to $98,000. 
<br><br>
<b>Angel investors</b> – Investors who provide investment and other support to early-stage businesses. Traditionally angels are wealthy individuals who have a significant amount of entrepreneurial, industry or investment experience.
<br><br>
<b>Angel network (or Angel syndicate)</b> – A group of angel investors that pool together money and other resources to invest in, and provide support to, early-stage businesses.
<br><br>
<b>Articles of association</b> – One of the constitutional documents of a company that sets out its management and administrative structure.  The articles dictate the internal affairs of the company such as director and shareholder rights, the issue and transfer of shares, and the organisation of meetings.
<br><br>
<b>As is</b> – Without Guarantees as to condition, as in a sale.  May signal a problem in condition, or may merely indicate that the seller is not in a position to attest as to the property's condition. 
<br><br>
<b>Asset class</b> – A class of economic property that has similar characteristics.  Listed shares, government bonds and real estate are all asset classes.
<br><br>
<b>B shares</b> – A class of shares which have specific rights attached to them, as set out in the company’s articles of association.  Typically B shares do not have voting rights or other benefits as compared to A shares.
<br><br>
<b>Balance</b> – The amount left over after subtracting the amount owed (on a loan) or the amount remaining already paid (in an account).
<br><br>
<b>Beneficial shareholder / owner</b> – An investor who owns the economic value and other shareholder benefits attached to shares, such as dividends  and tax reliefs, but the registered title to their shares is held with another person or entity, often for administrative convenience.
<br><br>
<b>Bootstrapping</b> – Starting and running a business with no external equity or debt financing.
<br><br>
<b>Burn rate</b> – The rate at which a company spends its cash.
<br><br>
<b>Buy & Hold</b> – Refers to longer-term equity investing, meaning that investors will take an ownership stake in a property (as opposed to being a lender or other lienholder) and hold that position for some period of years while the property is managed toward improved performance and returns.  "Buy and hold" investments can be contrasted to "fix and flip" purchases, where investors seek only to make some basic repairs or improvements and then to sell the property as quickly as possible.
<br><br>
<b>Buy Sell Clause</b> – An agreement among partners, LLC members, or stockholders pursuant to which some partners / members / stockholders agree to buy the interests of others, upon some event.
<br><br>
<b>Capital Call</b> – A request made to existing equity owners for additional money in order to fund deficits due to construction or operating costs.
<br><br>
<b>Capital Gain</b> – Gain on the sale of a capital asset like real property.  Capital gains enjoyed on assets held for a long term (generally at least one year) often enjoy lower tax rates than ordinary income.
<br><br>
<b>Capitalisation table (also called cap table)</b> – A table or chart that records all of the shareholders of a company and their percentage of equity, calculated on a fully-diluted basis.
<br><br>
<b>Cash flow</b> – Periodic payments available to an equity investor, after deducting all other expenses applicable to rental income, including operating and financing expenses.  Can be contrasted with net operating income, which deducts from gross rental income the various operating expenses, but which does not also factor in the effect of financing expenses.
<br><br>
<b>Cash return</b> – The "cash on cash return," also sometimes called the "equity dividend rate", is a simple ratio measurement of an investor's return in relation to the cash actually invested.  The cash on cash ratio is determined by dividing the before-tax cash flow (net operating income less debt payments) and dividing it by the initial equity investment.  For example if the net operating income is $150,000, the debt service is $50,000, and the amount initially invested is $1 million, then: <br>
(Net operating income less debt service) / initial equity investment = ($150,000 - 50,000) / $1,000,000 = 10% cash on cash return
<br><br>
