(Net operating income less debt service) / initial equity investment = ($150,000 - 50,000) / $1,000,000 = 10% cash on cash return
<br><br>
<b>Comparative Market Analysis</b> – Sometimes called a competitive market analysis, this is an estimate of the value of a property using some indicators taken from sales of comparable businesses.
<br><br>
<b>Compound Interest</b> – Interest paid on the original principal and also on any accumulated (accrued) and unpaid interest.
<br><br>




<b>Convertible equity</b> – An equity investment where money is invested in a company in exchange for shares to be issued at a later date.  The share issue is generally triggered by the company raising finance from other investors.  In return for investing early, the convertible equity investors receive a discount on the price of the shares issued to the other investors.
<br><br>
<b>Convertible note</b> – A debt investment where money is invested in a company with the expectation that the debt will “convert” into shares issued at a later date.  The share issue is generally triggered by the company raising finance from other investors.  Before the conversion, the investor is paid interest.
<br><br>
<b>Credit Score</b> – A number that purports to predict the probability that a person will repay a loan.  Generally, the higher the number, the better risk the individual is considered to be/  The score may determine whether the person gets the loan and how favorable the terms will be.  The score is estimated from information contained in the individual's credit report.
<br><br>
<b>Crowdfunding</b> – The funding of projects or ventures by raising money from a large number of people, usually online.  The three main types of crowdfunding are equity, debt and rewards/donations.
<br><br>
<b>Debt</b> – Money owed by one person/company to another. The borrower has to repay the money a later date, and generally also has to pay interest. .
<br><br>
<b>Dilution</b> – A reduction in the ownership percentage of a share in a company caused by the issue of new shares.
<br><br>
<b>Diversification</b> – An investment strategy  that involves mixing the amount, values and kinds of investments within a portfolio to spread risk and minimise losses.
<br><br>
<b>Dividends</b> – The distribution of a portion of a company’s profits to investors.
<br><br>
<b>Drag-along right</b> – A contractual obligation that allows majority shareholders to force minority shareholders to join in the sale of a company on the same terms, valuation and conditions of the majority shareholders.
<br><br>
<b>Equity</b> – Shares or other securities that represent an ownership interest in a company.
<br><br>
<b>Equity crowdfunding</b> – A type of crowdfunding that enables multiple investors to a buy shares, or other equity interests, in a company, usually through an online process.
<br><br>
<b>Exit</b> – An event when investors may be able to cash in and sell their shares, such as  an initial public offering (IPO) or trade sale.
<br><br>
<b>Fully diluted</b> – All the shares of a company in issue, plus all shares which are the subject of options or other contractual rights to be issued in the future (regardless of whether the right has vested).
<br><br>
<b>Fund</b>  – An investment opportunity that seeks to raise money to be invested across multiple businesses.  Funds campaigns are commonly used to invest in businesses participating in accelerator programmes and competition winners.
<br><br>
<b>Growth-stage</b> – The stage that a business is at when it has passed its seed or initial stage, has established proof of concept and is looking to grow.
<br><br>
<b>Initial public offering (IPO)</b> – The first time that a company’s shares are available for public purchase by means of a listing on a stock exchange.  This process is also known as going public or floating.
<br><br>
<b>Know Your Client (KYC)</b> – The regulatory process that financial services firms and certain other of businesses must perform to verify the identity of their customers to help prevent against money laundering and other financial crimes.
<br><br>
<b>Nominee</b> – A person or firm that holds assets such as shares on behalf of another, enabling the nominee to handle complicated administrative matters.
<br><br>
<b>Ordinary shares</b> – Shares which represent normal equity ownership in a company. Ordinary shares generally entitle the owner to vote at shareholder meetings, receive dividends, and receive distributions on the winding up of a company, but do not carry preferential treatment.
<br><br>
<b>Option</b> – A right granted which gives the receiver the option, but not the obligation, to buy (or sell) shares in a company, or other securities, at an agreed price within a certain time frame.
<br><br>
<b>Pre-emption (Also called anti-dilution)</b> – A contractual provision which requires the company to offer its shareholders the chance to purchase additional shares to maintain their percentage of equity in advance of further shares being issued.
<br><br>
<b>Post-investment</b> – The period of time after an investment has been made in a company.
<br><br>
<b>Portfolio</b> – A group of financial assets such as shares, property or bonds, held by one person or entity.
<br><br>
<b>Preference shares</b> – A class of shares which have specific preferential rights attached to them, as set out in the company’s articles of association.  Typically the preference will be a dividend paid in priority to other shareholders, or priority to distributions on the winding up of the company.
<br><br>
<b>Risk</b> – The potential for losing something of value. With equity investment the main risk to the investor is losing all the money invested.
<br><br>
<b>Shareholder agreement</b> – An agreement between a company’s shareholders detailing certain rights and obligations of the shareholders.
<br><br>
<b>Shares</b> – An ownership interest in a company which entitles the shareholder to certain rights, for example a share of profits or dividend payments from the company. Shares are also referred to as “stock”.
<br><br>
<b>Secondary market</b> – A market where investors purchase shares from other investors rather than from the company that has issued the shares directly.
<br><br>
<b>Seed-stage</b>  – The initial stage of a business, where it is looking to create a minimum viable product establish proof of concept.
<br><br>
<b>Subscription agreement</b> – An agreement between a company and investors purchasing shares in the company.  It sets out the terms of the share purchase and details certain rights and obligations of the company and the investors as shareholders.
<br><br>
<b>Tag-along rights</b> – A contractual obligation which gives minority shareholders the right, but not the obligation, to join a transaction where shares are sold by majority shareholders, on the same terms, valuation and conditions of the majority shareholders.
<br><br>
<b>Term sheet</b> – A non-binding agreement addressing the basic terms and conditions under which an investment will be made in a business.  A term sheet often serves as a template to develop more detailed legal investment documentation.
<br><br>
<b>Valuation</b> – The monetary worth of a business as determined by considering both qualitative and quantitative factors.
